#==============================================================================
# ** BaseItemFilter
#------------------------------------------------------------------------------
#  Represents a BaseItem filter
#==============================================================================

class BaseItemFilter < Filter
  
  #//////////////////////////////////////////////////////////////////////////
  # * Public Methods
  #//////////////////////////////////////////////////////////////////////////
  
  #--------------------------------------------------------------------------
  # * Apply method
  #     x : object to filter
  #--------------------------------------------------------------------------
  def apply(x)
    if x != nil && x.is_a?(RPG::BaseItem)
       
      case mode
        when "name"
          return applyName(x)
        #when
          #...
        else
          return true
      end
      
    else
      return nil
    end
  end
  
  #//////////////////////////////////////////////////////////////////////////
  # * Private Methods
  #//////////////////////////////////////////////////////////////////////////
  
  #--------------------------------------------------------------------------
  # * Apply method (using the name property)
  #     x : object to filter
  #--------------------------------------------------------------------------
  def applyName(x)
    if x.name == @value
      return true
    else
      return false
    end
  end
  private:applyName
  
end
